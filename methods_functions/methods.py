"""
Methods are essentially functions that are built into objects
"""

"""
Append a value onto a list - append method
"""

mylist1 = [1,2,3,4,5]

mylist1.append(6)
print(mylist1)


"""
Pop a value out of a list - Pop method
"""

mylist1.pop()
print(mylist1)

